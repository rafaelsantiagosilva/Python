def metade(num):
     '''
     Calcula a metade do numero passado
     '''
     return num/2

def dobro(num):
     '''
     Calcula o dobro do numero passado
     '''
     return num*2

def aumentar(num):
     '''
     Calcula o numero + 10% dele mesmo
     '''
     return num+(num/100*10)

def diminuir(num):
     '''
     Calcula o numero - 10% dele mesmo
     '''
     return num-(num/100*10)